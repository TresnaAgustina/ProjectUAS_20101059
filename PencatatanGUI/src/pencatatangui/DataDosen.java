    private void ShowData(String sql){
        try{
            Statement stmt = cnn.koneksi().createStatement();
            this.rs = stmt.executeQuery(sql);
        }catch(Exception e){
            System.out.println("Terjadi Kendala pada query");
        }
    }
    private void GetData(){
        String sql = "Select nidn, nama, no_tlp, tgl_lahir, jk, pendidikan, status_ikatan FROM dosen;";
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try{
            this.ShowData(sql);
            while(this.rs.next()){
                Object[] obj = new Object[7];
                obj[0] = this.rs.getString("nidn");
                obj[1] = this.rs.getString("nama");
                obj[2] = this.rs.getString("no_tlp");
                obj[3] = this.rs.getString("tgl_lahir");
                obj[5] = this.rs.getString("pendidikan");
                obj[6] = this.rs.getString("status_ikatan");
                String txJK = this.rs.getString("jk");
                if(txJK.equals("L")){
                    obj[4] = "Laki-Laki";
                }else{
                    obj[4] = "Perempuan";
                }
                model.addRow(obj);
            }
        }catch(Exception e){
        }
    }
    
    private boolean UpdateData(String SQL){
        boolean hsl = false;
        try{
            Statement stmt = cnn.koneksi().createStatement();
            stmt.executeUpdate(SQL);
            hsl = true;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Terjadi kendala pada instruksi SQL");
        }
        return hsl;
    }
    

