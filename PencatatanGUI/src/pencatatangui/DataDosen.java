    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        String NIDN = this.Fnidn.getText();
        String NAMA = this.Fnama.getText();
        String NO_TLP = this.Fno_tlp.getText();
        String TGL_LAHIR = this.Ftgl_lahir.getText();
        String PENDIDIKAN = this.Fpendidikan.getSelectedItem().toString();
        String STATUS = this.Fstatus.getSelectedItem().toString();
        String JKEL = "L";
        if(this.FjkP.isSelected()){
            JKEL = "P";
        }
        String SQLInsert = "INSERT INTO dosen(nidn,nama,no_tlp,tgl_lahir,jk,pendidikan,status_ikatan) VALUES('"+NIDN+"','"+NAMA+"','"+NO_TLP+"','"+TGL_LAHIR+"','"+JKEL+"','"+PENDIDIKAN+"','"+STATUS+"');";
        if(this.UpdateData(SQLInsert)){
            this.GetData();
            JOptionPane.showMessageDialog(null, "Data telah di Tambahkan");
        }
        this.BtnSave.setEnabled(false);
        this.BtnCancel.setVisible(false);
        this.BtnClose.setEnabled(true);
        this.BtnAddNew.setEnabled(true);
    }//GEN-LAST:event_BtnSaveActionPerformed

